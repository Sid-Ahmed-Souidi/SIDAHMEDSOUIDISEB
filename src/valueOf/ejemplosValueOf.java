package valueOf;

public class ejemplosValueOf {

	public static void main(String[] args) {
		
		
		char miarray[] = {'A','V','I','L','A'};
		int numero =232;
		String numeroString ="12";
		
		System.out.println(String.valueOf(miarray));
		System.out.println(String.valueOf(numero));
		System.out.println(Integer.valueOf(numeroString));
		System.out.println(Double.valueOf(numeroString));


	}

}
