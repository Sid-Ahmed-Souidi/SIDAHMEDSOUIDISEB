package unidad3.ejemplos;

public class BucleDoWhile {

	public static void main(String[] args) {


		System.out.println("BUCLE DO-WHILE");
		int n=10 ;

		do {

			System.out.println("Numero"+n);
			n--;


			}while(n>0);


		System.out.println("BUCLE WHILE");
		n=10;

		while(n>11) {

			System.out.println("Numero"+n);
			n--;

		}




	}




}
