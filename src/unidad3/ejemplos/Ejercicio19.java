package unidad3.ejemplos;

public class Ejercicio19 {

	public static void main(String[] args) {
		int numero = 0 ;
		int sumatorio = 0 ;
		

		while(numero <= 100) {
			
			sumatorio += numero;
			numero++;
		}
		System.out.println("La suma de los numeros del 1 al 100 es  :"+sumatorio);
		
		
	}

}
