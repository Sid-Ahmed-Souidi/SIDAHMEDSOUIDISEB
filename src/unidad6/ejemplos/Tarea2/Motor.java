package unidad6.ejemplos.Tarea2;

public class Motor {
	
	private int litrosAceite;
	private int cv;
	
	
	// constructor 
	public Motor(int litrosAceite) {
		
		this.litrosAceite = litrosAceite;
		
	}

	// getters y setters

	public int getLitrosAceite() {
		return litrosAceite;
	}


	public void setLitrosAceite(int litrosAceite) {
		this.litrosAceite = litrosAceite;
	}


	public int getCv() {
		return cv;
	}
	

}
